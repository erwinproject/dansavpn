clear
user=$1
masaaktif=$2
exp=$(grep -wE "^#& $user" "/usr/local/etc/xray/config.json" | cut -d ' ' -f 3 | sort | uniq)
now=$(date +%Y-%m-%d)
d1=$(date -d "$exp" +%s)
d2=$(date -d "$now" +%s)
exp2=$(( (d1 - d2) / 86400 ))
exp3=$(($exp2 + $masaaktif))
exp4=`date -d "$exp3 days" +"%Y-%m-%d"`
sed -i "/#& $user/c\#& $user $exp4" /usr/local/etc/xray/config.json
systemctl restart xray
FEEDBACK='{"id":"'${user}'","expired":"'${exp4}'","status": true,"msg":"Success"}'
echo $FEEDBACK | python3 -mjson.tool